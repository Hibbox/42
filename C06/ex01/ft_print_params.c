#include <unistd.h>

int main (int argc, char **argv)
{       
	int j;
        int i;

        i = 0;

	j = 1;
	while (j < argc)
	{
		i = 0;
        	while (argv[j][i] != '\0')
        	{
                	write(1, &argv[j][i],1);
                	i++;
        	}
		j++;
	}
}

